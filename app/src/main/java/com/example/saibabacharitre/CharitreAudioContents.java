package com.example.saibabacharitre;

public class CharitreAudioContents {

    private String ChapterNumber1;
    private String ChapterName1;


    public CharitreAudioContents(String chapterNumber1, String chapterName1) {
        ChapterNumber1 = chapterNumber1;
        ChapterName1 = chapterName1;
    }

    public String getChapterNumber1() {
        return ChapterNumber1;
    }

    public String getChapterName1() {
        return ChapterName1;
    }

    public void setChapterNumber1(String chapterNumber1) {
        ChapterNumber1 = chapterNumber1;
    }

    public void setChapterName1(String chapterName1) {
        ChapterName1 = chapterName1;
    }
}
